package academy.learnprogramming;

public class Main {

    public static void main(String[] args) {
        int i = 0;
	    while(i < 6) {
            System.out.println("value = " + i);
	        i++;
        }

	    int count = 1 ;
	    do {
            System.out.println("value = " + count);
            count++;
        } while (count < 6 );
    }
}
